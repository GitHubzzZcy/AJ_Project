package com.henghao.entity;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;
/**
 * 事件信息保存  ------>该类已注销实体信息在UserEventTrackEntity类
 * @author Administrator
 *
 */
@Entity
@Table(name="aj_grgj" )
public class EventInformation {
	/**
	 * ID
	 */
	private Integer ID;
	/**
	 * userID
	 */
	private String USERID;
	/**
	 * 日期
	 */
	private String TIME;
	/**
	 * 事件名称
	 */
	private String SHIJIAN_EVENT;
	/**
	 * 地点
	 */
	private String SHIJIAN_PLACE;
	/**
	 * 上传时间
	 */
	private String SHIJIAN_TIME;
	/**
	 * 事件编码
	 */
	private String TOKEN;
	@Id
	@GeneratedValue(strategy=GenerationType.IDENTITY)
	public Integer getID() {
		return ID;
	}
	public void setID(Integer iD) {
		ID = iD;
	}
	public String getUSERID() {
		return USERID;
	}
	public void setUSERID(String uSERID) {
		USERID = uSERID;
	}
	public String getTIME() {
		return TIME;
	}
	public void setTIME(String tIME) {
		TIME = tIME;
	}
	public String getSHIJIAN_EVENT() {
		return SHIJIAN_EVENT;
	}
	public void setSHIJIAN_EVENT(String sHIJIAN_EVENT) {
		SHIJIAN_EVENT = sHIJIAN_EVENT;
	}
	public String getSHIJIAN_PLACE() {
		return SHIJIAN_PLACE;
	}
	public void setSHIJIAN_PLACE(String sHIJIAN_PLACE) {
		SHIJIAN_PLACE = sHIJIAN_PLACE;
	}
	public String getSHIJIAN_TIME() {
		return SHIJIAN_TIME;
	}
	public void setSHIJIAN_TIME(String sHIJIAN_TIME) {
		SHIJIAN_TIME = sHIJIAN_TIME;
	}
	public String getTOKEN() {
		return TOKEN;
	}
	public void setTOKEN(String tOKEN) {
		TOKEN = tOKEN;
	}
	
	
}
