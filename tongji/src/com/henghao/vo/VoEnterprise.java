package com.henghao.vo;

import java.math.BigDecimal;


/**
 * 企业包装实体类
 * @author 王章鹏
 * @time 2017-4-13
 */
public class VoEnterprise {

	//企业ID
	private String ENTERPRISEID;
	//企业名称
	private String ENTNAME;
	//单位经度
	private String LONGITUDE;
	//单位纬度
	private String LATITUDE;
	//注册地址
	private String REGADDRESS;
	//企业类型
	private String TYPENAME;
	//存在隐患的级别 		1:一般隐患，2：重大隐患
	private BigDecimal HIDDENLEVEL;
	//隐患来源		1:自查自报，2：监督检查，3：举报
	private BigDecimal FINDHIDDENSTYLE;
	//隐患描述
	private String HIDDENDES;
	
	//get and set method
	public String getENTERPRISEID() {
		return ENTERPRISEID;
	}
	public void setENTERPRISEID(String eNTERPRISEID) {
		ENTERPRISEID = eNTERPRISEID;
	}
	public String getENTNAME() {
		return ENTNAME;
	}
	public void setENTNAME(String eNTNAME) {
		ENTNAME = eNTNAME;
	}
	public String getLONGITUDE() {
		return LONGITUDE;
	}
	public void setLONGITUDE(String lONGITUDE) {
		LONGITUDE = lONGITUDE;
	}
	public String getLATITUDE() {
		return LATITUDE;
	}
	public void setLATITUDE(String lATITUDE) {
		LATITUDE = lATITUDE;
	}
	public String getREGADDRESS() {
		return REGADDRESS;
	}
	public void setREGADDRESS(String rEGADDRESS) {
		REGADDRESS = rEGADDRESS;
	}
	public String getTYPENAME() {
		return TYPENAME;
	}
	public void setTYPENAME(String tYPENAME) {
		TYPENAME = tYPENAME;
	}
	public BigDecimal getHIDDENLEVEL() {
		return HIDDENLEVEL;
	}
	public void setHIDDENLEVEL(BigDecimal hIDDENLEVEL) {
		HIDDENLEVEL = hIDDENLEVEL;
	}
	public BigDecimal getFINDHIDDENSTYLE() {
		return FINDHIDDENSTYLE;
	}
	public void setFINDHIDDENSTYLE(BigDecimal fINDHIDDENSTYLE) {
		FINDHIDDENSTYLE = fINDHIDDENSTYLE;
	}
	public String getHIDDENDES() {
		return HIDDENDES;
	}
	public void setHIDDENDES(String hIDDENDES) {
		HIDDENDES = hIDDENDES;
	}
	
	//空构造
	public VoEnterprise() {
		
	}
	
	
	//全参数构造
	public VoEnterprise(String eNTERPRISEID, String eNTNAME, String lONGITUDE,
			String lATITUDE, String rEGADDRESS, String tYPENAME,
			BigDecimal hIDDENLEVEL, BigDecimal fINDHIDDENSTYLE, String hIDDENDES) {
		super();
		ENTERPRISEID = eNTERPRISEID;
		ENTNAME = eNTNAME;
		LONGITUDE = lONGITUDE;
		LATITUDE = lATITUDE;
		REGADDRESS = rEGADDRESS;
		TYPENAME = tYPENAME;
		HIDDENLEVEL = hIDDENLEVEL;
		FINDHIDDENSTYLE = fINDHIDDENSTYLE;
		HIDDENDES = hIDDENDES;
	}
	
	//toString
	@Override
	public String toString() {
		return "VoEnterprise [ENTERPRISEID=" + ENTERPRISEID + ", ENTNAME="
				+ ENTNAME + ", LONGITUDE=" + LONGITUDE + ", LATITUDE="
				+ LATITUDE + ", REGADDRESS=" + REGADDRESS + ", TYPENAME="
				+ TYPENAME + ", HIDDENLEVEL=" + HIDDENLEVEL
				+ ", FINDHIDDENSTYLE=" + FINDHIDDENSTYLE + ", HIDDENDES="
				+ HIDDENDES + "]";
	}
	
	
}
