package com.henghao.vo;

import java.math.BigDecimal;
import java.util.Date;

/** 
* @date 2017-5-27  ----创建时间
* <pre>
* Title:  执法业务信息
* Description:  查询执法业务信息包装类
* Company:  贵州恒昊软件科技有限公司
*</pre>
* @author 王章鹏
*/
public class CaseVo {
	// 案件ID
	private String ID;
	// 数据来源类型(1从检查登记来，2直接登记)
	private BigDecimal DATASOURCETYPE;
	// 案件处理类型(1简易程序，2一般程序，3限期整改)
	private BigDecimal CASETYPE;
	// 案件业务来源(1执法检查，2事故，3上级交办，4举报，5下级报请)
	private BigDecimal CASESOURCETYPE;
	// 案件名称
	private String CASENAME;
	// 案件当事人(是企业时，为企业名，是个人时为人名)
	private String CASEPARTY;
	// 所属区域
	private String DISTRICT;
	// 建立时间
	private String CREATETIME;
	// 创建机构
	private String ORG;
	// 创建部门
	private String DEPT;
	// 创建人
	private String EMPNAME;
	// 案件类型( 1.安全隐患案件 2.违法行为案件 3.安全事故案件 )
	private BigDecimal TYPE;

	// set and get
	public String getID() {
		return ID;
	}

	public void setID(String iD) {
		ID = iD;
	}

	public BigDecimal getDATASOURCETYPE() {
		return DATASOURCETYPE;
	}

	public void setDATASOURCETYPE(BigDecimal dATASOURCETYPE) {
		DATASOURCETYPE = dATASOURCETYPE;
	}

	public BigDecimal getCASETYPE() {
		return CASETYPE;
	}

	public void setCASETYPE(BigDecimal cASETYPE) {
		CASETYPE = cASETYPE;
	}

	public BigDecimal getCASESOURCETYPE() {
		return CASESOURCETYPE;
	}

	public void setCASESOURCETYPE(BigDecimal cASESOURCETYPE) {
		CASESOURCETYPE = cASESOURCETYPE;
	}

	public String getCASENAME() {
		return CASENAME;
	}

	public void setCASENAME(String cASENAME) {
		CASENAME = cASENAME;
	}

	public String getCASEPARTY() {
		return CASEPARTY;
	}

	public void setCASEPARTY(String cASEPARTY) {
		CASEPARTY = cASEPARTY;
	}

	public String getDISTRICT() {
		return DISTRICT;
	}

	public void setDISTRICT(String dISTRICT) {
		DISTRICT = dISTRICT;
	}

	public String getCREATETIME() {
		return CREATETIME;
	}

	public void setCREATETIME(Date cREATETIME) {
		CREATETIME = cREATETIME.toString();
	}

	public String getORG() {
		return ORG;
	}

	public void setORG(String oRG) {
		ORG = oRG;
	}

	public String getDEPT() {
		return DEPT;
	}

	public void setDEPT(String dEPT) {
		DEPT = dEPT;
	}

	public String getEMPNAME() {
		return EMPNAME;
	}

	public void setEMPNAME(String eMPNAME) {
		EMPNAME = eMPNAME;
	}

	public BigDecimal getTYPE() {
		return TYPE;
	}

	public void setTYPE(BigDecimal tYPE) {
		TYPE = tYPE;
	}

}