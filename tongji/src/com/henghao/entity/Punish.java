package com.henghao.entity;
/**
 * 处罚偏离度
 * @author 龙宏
 *
 */
public class Punish {
	/**
	 * 处罚最大金额
	 */
 private String fineMaxMoney;
 /**
  * 处罚最小金额
  */
 private String fineSizeMoney;
 /**
  * 实际处罚金额
  */
 private String factMoney;
 /**
  * 处罚偏离状态
  */
 private String state;

public String getState() {
	return state;
}
public void setState(String state) {
	this.state = state;
}
public String getFineMaxMoney() {
	return fineMaxMoney;
}
public void setFineMaxMoney(String fineMaxMoney) {
	this.fineMaxMoney = fineMaxMoney;
}
public String getFineSizeMoney() {
	return fineSizeMoney;
}
public void setFineSizeMoney(String fineSizeMoney) {
	this.fineSizeMoney = fineSizeMoney;
}
public String getFactMoney() {
	return factMoney;
}
public void setFactMoney(String factMoney) {
	this.factMoney = factMoney;
}
 
}
