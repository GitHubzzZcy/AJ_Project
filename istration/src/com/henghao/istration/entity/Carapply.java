package com.henghao.istration.entity;
/**
 * 用车申请单信息
 * @author 周承耀
 *
 */
public class Carapply {
	 
	//id
	private String ID;
	//车牌
	private String CARNO;
	//用车人
	private String USERS_NAME;
	//申请时间
	private String CREATED;
	//出发时间
	private String STARTTIME;
	//返程时间
	private String ENDTIME;
	//目的地
	private String DESTINATIONS;
	//联系电话
	private String TEL;
	//偏航统计
	private String sheer;
	
	
	
	public String getSheer() {
		return sheer;
	}
	public void setSheer(String sheer) {
		this.sheer = sheer;
	}
	public String getUSERS_NAME() {
		return USERS_NAME;
	}
	public void setUSERS_NAME(String uSERS_NAME) {
		USERS_NAME = uSERS_NAME;
	}
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public String getCARNO() {
		return CARNO;
	}
	public void setCARNO(String cARNO) {
		CARNO = cARNO;
	}
	public String getCREATED() {
		return CREATED;
	}
	public void setCREATED(String cREATED) {
		CREATED = cREATED;
	}
	public String getSTARTTIME() {
		return STARTTIME;
	}
	public void setSTARTTIME(String sTARTTIME) {
		STARTTIME = sTARTTIME;
	}
	public String getENDTIME() {
		return ENDTIME;
	}
	public void setENDTIME(String eNDTIME) {
		ENDTIME = eNDTIME;
	}
	public String getDESTINATIONS() {
		return DESTINATIONS;
	}
	public void setDESTINATIONS(String dESTINATIONS) {
		DESTINATIONS = dESTINATIONS;
	}
	public String getTEL() {
		return TEL;
	}
	public void setTEL(String tEL) {
		TEL = tEL;
	}
	@Override
	public String toString() {
		return "Carapply [ID=" + ID + ", CARNO=" + CARNO + ", CREATED="
				+ CREATED + ", STARTTIME=" + STARTTIME + ", ENDTIME=" + ENDTIME
				+ ", DESTINATIONS=" + DESTINATIONS + ", TEL=" + TEL + "]";
	}
	public Carapply(String iD, String cARNO, String uSERS_NAME, String cREATED,
			String sTARTTIME, String eNDTIME, String dESTINATIONS, String tEL,
			String sheer) {
		super();
		ID = iD;
		CARNO = cARNO;
		USERS_NAME = uSERS_NAME;
		CREATED = cREATED;
		STARTTIME = sTARTTIME;
		ENDTIME = eNDTIME;
		DESTINATIONS = dESTINATIONS;
		TEL = tEL;
		this.sheer = sheer;
	}
	public Carapply() {
		super();
	}
	
	
}
