package com.henghao.news.entity;

/**
 * 用车申请实体类
 * @Description:
 * @author zcy  
 * @date 2017年11月28日 上午10:36:54
 * @version 1.0
 */
public class CarBookEntity {

	
	
	private String ID; 
	private String USERS; //用车人ID   # 前面有加U_
	private String CARNO; //车牌号
	private String TITLE;  //用途
	private String STARTTIME;  //开始时间
	private String ENDTIME; //结束时间
	private String ORIGIN; //出发地
	private String DESTINATIONS; //目的地
	private String TRAVELCATEGOR;//行驶范围
	
	
	public CarBookEntity() {
		super();
	}
	public CarBookEntity(String iD, String uSERS, String cARNO, String tITLE, String sTARTTIME, String eNDTIME,
			String oRIGIN, String dESTINATIONS, String tRAVELCATEGOR) {
		super();
		ID = iD;
		USERS = uSERS;
		CARNO = cARNO;
		TITLE = tITLE;
		STARTTIME = sTARTTIME;
		ENDTIME = eNDTIME;
		ORIGIN = oRIGIN;
		DESTINATIONS = dESTINATIONS;
		TRAVELCATEGOR = tRAVELCATEGOR;
	}
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public String getUSERS() {
		return USERS;
	}
	public void setUSERS(String uSERS) {
		USERS = uSERS;
	}
	public String getCARNO() {
		return CARNO;
	}
	public void setCARNO(String cARNO) {
		CARNO = cARNO;
	}
	public String getTITLE() {
		return TITLE;
	}
	public void setTITLE(String tITLE) {
		TITLE = tITLE;
	}
	public String getSTARTTIME() {
		return STARTTIME;
	}
	public void setSTARTTIME(String sTARTTIME) {
		STARTTIME = sTARTTIME;
	}
	public String getENDTIME() {
		return ENDTIME;
	}
	public void setENDTIME(String eNDTIME) {
		ENDTIME = eNDTIME;
	}
	public String getORIGIN() {
		return ORIGIN;
	}
	public void setORIGIN(String oRIGIN) {
		ORIGIN = oRIGIN;
	}
	public String getDESTINATIONS() {
		return DESTINATIONS;
	}
	public void setDESTINATIONS(String dESTINATIONS) {
		DESTINATIONS = dESTINATIONS;
	}
	public String getTRAVELCATEGOR() {
		return TRAVELCATEGOR;
	}
	public void setTRAVELCATEGOR(String tRAVELCATEGOR) {
		TRAVELCATEGOR = tRAVELCATEGOR;
	}
	
	
}
