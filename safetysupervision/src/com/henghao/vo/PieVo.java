package com.henghao.vo;

/**
 * @author wzp
 * @description 执法饼图-数据信息查询-接收类
 * @create on 2017/12/11.
 */
public class PieVo {

    private String SID;     // aj_zfdata中的ID
    private String SFYH;    // 是否隐患
    private String YHSM;    // 隐患说明
    private String ZFJG;    // 执法结果
    private String FILE;    // 执法影像（图片）

    public String getSID() {
        return SID;
    }

    public void setSID(String SID) {
        this.SID = SID;
    }

    public String getSFYH() {
        return SFYH;
    }

    public void setSFYH(String SFYH) {
        this.SFYH = SFYH;
    }

    public String getYHSM() {
        return YHSM;
    }

    public void setYHSM(String YHSM) {
        this.YHSM = YHSM;
    }

    public String getZFJG() {
        return ZFJG;
    }

    public void setZFJG(String ZFJG) {
        this.ZFJG = ZFJG;
    }

    public String getFILE() {
        return FILE;
    }

    public void setFILE(String FILE) {
        this.FILE = FILE;
    }

    @Override
    public String toString() {
        return "PiEntity{" +
                "SID='" + SID + '\'' +
                ", SFYH='" + SFYH + '\'' +
                ", YHSM='" + YHSM + '\'' +
                ", ZFJG='" + ZFJG + '\'' +
                ", FILE='" + FILE + '\'' +
                '}';
    }
}
