package com.henghao.vo;

/**
 * @author wzp
 * @description: 排行包装类
 * @create on 2017/12/6.
 */
public class RankVo {

    private String dept;    // 部门
    private int count;   // 执法数量

    public String getDept() {
        return dept;
    }

    public void setDept(String dept) {
        this.dept = dept;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    /**
     * 空构造
     */
    public RankVo() {
    }

    /**
     * 全构造
     */
    public RankVo(String dept, int cout) {
        this.dept = dept;
        this.count = cout;
    }
}
