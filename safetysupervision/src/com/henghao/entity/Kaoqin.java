package com.henghao.entity;

/**
 * 考勤查询接收的实体类
 * @author NWL
 *
 */
public class Kaoqin {
	private String STARDATE;
	private String ENDDATE;
	private String WEEKEND;
	private String CLOCKINTIME;
	private String CLOCKOUTTIME;
	private String CKSTATUS;
	private String OTHER;
	
	public String getSTARDATE() {
		return STARDATE;
	}
	public void setSTARDATE(String sTARDATE) {
		STARDATE = sTARDATE;
	}
	public String getENDDATE() {
		return ENDDATE;
	}
	public void setENDDATE(String eNDDATE) {
		ENDDATE = eNDDATE;
	}
	public String getWEEKEND() {
		return WEEKEND;
	}
	public void setWEEKEND(String wEEKEND) {
		WEEKEND = wEEKEND;
	}
	public Kaoqin(String sTARDATE, String eNDDATE, String wEEKEND,
			String cLOCKINTIME, String cLOCKOUTTIME, String cKSTATUS,
			String oTHER) {
		super();
		STARDATE = sTARDATE;
		ENDDATE = eNDDATE;
		WEEKEND = wEEKEND;
		CLOCKINTIME = cLOCKINTIME;
		CLOCKOUTTIME = cLOCKOUTTIME;
		CKSTATUS = cKSTATUS;
		OTHER = oTHER;
	}
	public String getCLOCKINTIME() {
		return CLOCKINTIME;
	}
	public void setCLOCKINTIME(String cLOCKINTIME) {
		CLOCKINTIME = cLOCKINTIME;
	}
	public String getCLOCKOUTTIME() {
		return CLOCKOUTTIME;
	}
	public void setCLOCKOUTTIME(String cLOCKOUTTIME) {
		CLOCKOUTTIME = cLOCKOUTTIME;
	}
	public String getCKSTATUS() {
		return CKSTATUS;
	}
	public void setCKSTATUS(String cKSTATUS) {
		CKSTATUS = cKSTATUS;
	}
	public String getOTHER() {
		return OTHER;
	}
	public void setOTHER(String oTHER) {
		OTHER = oTHER;
	}
	public Kaoqin() {
		super();
	}
	@Override
	public String toString() {
		return "Kaoqin [STARDATE=" + STARDATE + ", ENDDATE=" + ENDDATE
				+ ", WEEKEND=" + WEEKEND + ", CLOCKINTIME=" + CLOCKINTIME
				+ ", CLOCKOUTTIME=" + CLOCKOUTTIME + ", CKSTATUS=" + CKSTATUS
				+ ", OTHER=" + OTHER + "]";
	}
	
}
